        --seqlen 2048 \
        --output_path $RES/$SAVE_NAME \
        --no_use_cuda_graph 2>&1 | tee -a $LOG/$SAVE_NAME.log

    if [ "$pretrain_path" != "$HF" ]; then
        rm -r "$pretrain_path"
        rm -r "$CKPT/$SAVE_NAME"
    fi

done

