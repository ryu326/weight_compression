def _to_optional_tensor(x: Optional[Any], length: int, device: torch.device, dtype: torch.dtype) -> Optional[torch.Tensor]:
    if x is None:
        return None
    if torch.is_tensor(x):
        t = x.to(device=device, dtype=dtype)
    else:
        t = torch.as_tensor(x, device=device, dtype=dtype)
    if t.ndim == 2 and t.shape == (1, length):
        t = t[0]
    if t.ndim == 2 and t.shape == (length, 1):
        t = t[:, 0]
    if t.ndim != 1 or t.numel() != length:
        raise ValueError(f"Expected 1D tensor with {length} elements, got {tuple(t.shape)}")
    return t


def _resolve_decoder_type(args) -> str:
    for key in ("ec_decoder_type", "decoder_type", "ecft_decoder_type"):
        value = getattr(args, key, None)
        if value is not None:
            return value
    return "rht"


def _resolve_rht_seed(args) -> Optional[int]:
    for key in ("ec_rht_seed", "rht_seed", "ecft_rht_seed"):
        if hasattr(args, key):
            return getattr(args, key)
    return 0

def _build_optimizer_args(args):
    return SimpleNamespace(
        learning_rate=float(getattr(args, "ecft_learning_rate", 1e-4)),
        aux_learning_rate=float(getattr(args, "ecft_aux_learning_rate", 1e-3)),
    )


def _rate_bits_per_param(likelihoods: torch.Tensor) -> torch.Tensor:
    return (-torch.log2(likelihoods.float().clamp_min(1e-9))).mean()


def _fuse_metadata_into_layer(
    layer: EntropyConstrainedLinear,
    metadata: dict[str, Any],
    args,
) -> bool:
    if not isinstance(metadata, dict):
        return False

    device = layer.latent.device
    dtype = layer.latent.dtype
    row_factor = torch.ones(layer.out_features, device=device, dtype=dtype)
    col_factor = torch.ones(layer.in_features, device=device, dtype=dtype)
    has_row = False
    has_col = False

    row_std = _to_optional_tensor(
        metadata.get("row_std"),
        layer.out_features,
        device,
        dtype,
    )
    if row_std is not None:
        if bool(getattr(args, "row_normalize2", False)):
            row_factor = row_factor * row_std
            has_row = True
        if bool(getattr(args, "row_normalize", False)) or bool(
            getattr(args, "normalization_search", False)
        ):
            row_factor = row_factor * row_std
            has_row = True

    col_std = _to_optional_tensor(
        metadata.get("col_std"),
        layer.in_features,
        device,
        dtype,
    )
    if col_std is not None and (
        bool(getattr(args, "col_normalize", False))
        or bool(getattr(args, "normalization_search", False))
    ):
        col_factor = col_factor * col_std
        has_col = True

    scale_h = _to_optional_tensor(
        metadata.get("scaleH"),
        layer.in_features,
        device,
        dtype,
    )
    if scale_h is not None:
        if bool(getattr(args, "scaleHinv", False)):
            col_factor = col_factor / scale_h
            has_col = True
        if bool(getattr(args, "scaleH", False)):
            col_factor = col_factor / scale_h
            has_col = True

    if not has_row and not has_col:
        return False

    layer.fuse_decoder_diagonal(
        row_scale=row_factor if has_row else None,
        col_scale=col_factor if has_col else None,
        inverse=False,
    )
    return True


def _search_qs_for_target_rate(
    layer: EntropyConstrainedLinear,
    args,
    device: torch.device,
    dtype: torch.dtype,
    log_path: Optional[str] = None,
) -> Optional[torch.Tensor]:
    target_rate = getattr(args, "R_target", None)
    if target_rate is None:
        return None

    best_qs, theory_rate, ecsq_mse = ecsq.binary_search_qs(
        layer.latent.detach(), target_rate=float(target_rate)
    )
    qs_tensor = torch.tensor(float(best_qs), device=device, dtype=dtype)
    layer.qs = qs_tensor
    if bool(getattr(args, "verbose", False)):
        glog.info(
            "[ECFT qs-search] "
            f"R_target={float(target_rate):.6f} "
            f"qs={float(best_qs):.6f} "
            f"theory_rate={float(theory_rate):.6f} "
            f"ecsq_mse={float(ecsq_mse):.6f}"
        )
    _ecft_log_append_qs_search(
        log_path,
        target_rate=float(target_rate),
        qs=float(best_qs),
        theory_rate=float(theory_rate),
        ecsq_mse=float(ecsq_mse),
    )
    return qs_tensor


def _clone_best_state(model: EntropyConstrainedLinear) -> dict[str, torch.Tensor]:
    state = {}
    for name, tensor in model.state_dict().items():
        state[name] = tensor.detach().cpu().clone()
    return state


def _ecft_log_path(args) -> Optional[str]:
    save_root = getattr(args, "res_path", None) or getattr(args, "save_path", None)
    if not save_root:
        return None
    layer_idx = getattr(args, "layer_idx", None)
    layer_name = getattr(args, "layer_name", None)
    if layer_idx is None or layer_name is None:
        return None
    log_dir = os.path.join(save_root, "ecft_log")
    os.makedirs(log_dir, exist_ok=True)
    return os.path.join(log_dir, f"{layer_idx}_{layer_name}.log")


def _ecft_log_init(path: Optional[str], epochs: int, lmbda: float, decoder_type: str) -> None:
    if path is None:
        return
    with open(path, "w", encoding="utf-8") as f:
        f.write(
            f"META epochs={epochs} lmbda={lmbda} decoder_type={decoder_type}\n"
        )
        f.write("STEP stage epoch loss mse_loss rate_loss aux_loss\n")


def _ecft_log_append_qs_search(
    path: Optional[str],
    target_rate: float,
    qs: float,
    theory_rate: float,
    ecsq_mse: float,
) -> None:
    if path is None:
        return
    with open(path, "a", encoding="utf-8") as f:
        f.write(
            "QS_SEARCH "
            f"R_target={target_rate:.8f} "
            f"qs={qs:.8f} "
            f"theory_rate={theory_rate:.8f} "
            f"ecsq_mse={ecsq_mse:.8f}\n"
        )


def _ecft_log_append_train(path: Optional[str], row: dict[str, float]) -> None:
    if path is None:
        return
    with open(path, "a", encoding="utf-8") as f:
        f.write(
            "STEP "
            f"{row['stage']} "
            f"{row['epoch']:.0f} "
            f"{row['loss']:.8f} "
            f"{row['mse_loss']:.8f} "
            f"{row['rate_loss']:.8f} "
            f"{row['aux_loss']:.8f}\n"
        )


def _ecft_log_append_final(
    path: Optional[str],
    mse_loss: float,
    rate_loss: float,
    bpp: float,
    decoder_bits_total: int,
) -> None:
    if path is None:
        return
    with open(path, "a", encoding="utf-8") as f:
        f.write(
            "FINAL "
            f"mse={mse_loss:.8f} "
            f"rate={rate_loss:.8f} "
            f"bpp={bpp:.8f} "
            f"decoder_bits_total={decoder_bits_total}\n"
        )


def _ecft_log_append_ecsq_compare(
    path: Optional[str],
    ecsq_compare: Any,
) -> None:
    if path is None:
        return
    with open(path, "a", encoding="utf-8") as f:
        f.write("ECSQ_COMPARE\n")
        if isinstance(ecsq_compare, dict) and ecsq_compare.get("ran", False):
            ec = ecsq_compare.get("ec", {})
            ecsq = ecsq_compare.get("ecsq", {})
            delta = ecsq_compare.get("delta", {})
            f.write(
                "ran=1 "
                f"target_rate={float(ecsq_compare.get('target_rate_from_ec', float('nan'))):.8f} "
                f"ec_mse={float(ec.get('mse', float('nan'))):.8f} "
                f"ec_rate={float(ec.get('rate_loss', float('nan'))):.8f} "
                f"ecsq_mse={float(ecsq.get('mse', float('nan'))):.8f} "
                f"ecsq_rate={float(ecsq.get('rate', float('nan'))):.8f} "
                f"delta_mse={float(delta.get('mse_ec_minus_ecsq', float('nan'))):.8f} "
                f"delta_rate={float(delta.get('rate_ec_minus_ecsq', float('nan'))):.8f}\n"
            )
            return
        if isinstance(ecsq_compare, dict):
            f.write(f"ran=0 reason={ecsq_compare.get('reason')}\n")
            return
        f.write(f"ran=- reason={ecsq_compare}\n")


