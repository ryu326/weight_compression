def ec_linear_train(W, H, args, device="cpu"):
    
    sigma_reg = getattr(args, "sigma_reg", 1e-2)
    if H is not None:
        n_h = H.shape[0]
        H = utils.regularize_H2(H.clone(), n_h, sigma_reg)
    
    target_dtype = torch.float32
    target = W.to(device=device, dtype=target_dtype)
    out_features, in_features = target.shape

    target, Hr, metadata = utils.standardize_W(target, H, args, device, None)

    epochs = int(getattr(args, "ecft_epochs", 0) or 0)
    lmbda = float(getattr(args, "ecft_lmbda", 0.0) or 0.0)
    decoder_type = _resolve_decoder_type(args)
    rht_seed = _resolve_rht_seed(args)
    quantize_mode = str(getattr(args, "ecft_mode", "noise"))
    if quantize_mode not in ("noise", "ste"):
        raise ValueError(
            f"--ecft_mode must be 'noise' or 'ste', got {quantize_mode!r}"
        )

    layer = _build_ec_layer(
        args,
        in_features=in_features,
        out_features=out_features,
        decoder_type=decoder_type,
        rht_seed=rht_seed,
        device=target.device,
        dtype=target.dtype,
    )
    layer.quantize_mode = quantize_mode
    encode_target = target

    layer.initialize_from_weight(encode_target)

    # Load pretrained Gaussian priors if path is set — this overwrites the
    # freshly-initialized qs / entropy_bottleneck / (B for lattice) with the
    # pre-learned values.
    applied_pretrained = _maybe_apply_pretrained(layer, args)

    history: list[dict[str, Any]] = []
    log_path = _ecft_log_path(args)
    _ecft_log_init(log_path, epochs=epochs, lmbda=lmbda, decoder_type=decoder_type)
    if applied_pretrained:
        # Use pretrained qs as-is; skip the per-latent binary search.
        qs = layer.qs
        glog.info(f"[ec pretrained] skip qs search, using pretrained qs={float(qs):.4f}")
    else:
        qs = _search_qs_for_target_rate(
            layer,
            args,
            device=target.device,
            dtype=target.dtype,
            log_path=log_path,
        )
        layer.qs = qs

    opt_args = _build_optimizer_args(args)
    optimizer, aux_optimizer = configure_ec_optimizers(layer, opt_args)

    warmup_steps = int(getattr(args, "ecft_aux_warmup_step", 0) or 0)
    warmup_rate_last = float("nan")
    warmup_aux_last = float("nan")
    if warmup_steps > 0:
        layer.train()
        log_every = max(1, warmup_steps // 5)
        # log_every = 1
        latent_requires_grad = layer.latent.requires_grad
        left_diag_requires_grad = layer.left_diag.requires_grad
        right_diag_requires_grad = layer.right_diag.requires_grad
        try:
            layer.latent.requires_grad_(False)
            layer.left_diag.requires_grad_(False)
            layer.right_diag.requires_grad_(False)
            with torch.enable_grad():
                for step in range(warmup_steps):
                    optimizer.zero_grad(set_to_none=True)
                    if aux_optimizer is not None:
                        aux_optimizer.zero_grad(set_to_none=True)

                    layer.quantize_latent(training=False, return_likelihoods=False)
                    rate_loss = layer._last_rate_loss
                    (10*rate_loss).backward()

                    aux_loss_value = torch.tensor(0.0, device=target.device)
                    if aux_optimizer is not None:
                        aux_loss_value = layer.aux_loss()
                        aux_loss_value.backward()

                    optimizer.step()
                    if aux_optimizer is not None:
                        aux_optimizer.step()

                    warmup_rate_last = float(rate_loss.item())
                    warmup_aux_last = float(aux_loss_value.item())
                    row = {
                        "stage": "warmup",
                        "epoch": 0.0,
                        "loss": warmup_rate_last,
                        "mse_loss": float("nan"),
                        "rate_loss": warmup_rate_last,
                        "aux_loss": warmup_aux_last,
                    }
                    history.append(row)
                    _ecft_log_append_train(log_path, row)

                    if bool(getattr(args, "verbose", False)) and (
                        ((step + 1) % log_every == 0) or (step == 0) or (step + 1 == warmup_steps)
                    ):
                        glog.info(
                            f"{args.layer_idx}_{args.layer_name} [ECFT warmup] step {step + 1}/{warmup_steps} "
                            f"rate={warmup_rate_last:.6f} aux={warmup_aux_last:.6f}"
                        )
        finally:
            layer.latent.requires_grad_(latent_requires_grad)
            layer.left_diag.requires_grad_(left_diag_requires_grad)
            layer.right_diag.requires_grad_(right_diag_requires_grad)

        layer.update_entropy_model(force=True, update_quantiles=True)

    best_state = _clone_best_state(layer)
    best_loss = math.inf

    target_rate_value = getattr(args, "R_target", None)
    use_adaptive_lambda = bool(getattr(args, "ecft_adaptive_lambda", False))
    if use_adaptive_lambda:
        if target_rate_value is None:
            raise ValueError(
                "ecft_adaptive_lambda=True requires --R_target to set target rate."
            )
        target_rate_value = float(target_rate_value)
        lambda_rate = float(lmbda)
        lambda_rate_lr = float(getattr(args, "ecft_lambda_lr", 0.1))
        lambda_rate_min = float(getattr(args, "ecft_lambda_min", 0.0))
        lambda_rate_max = float(getattr(args, "ecft_lambda_max", 1e8))
        rate_ema_beta = float(getattr(args, "ecft_rate_ema_beta", 0.9))
        rate_tolerance = float(getattr(args, "ecft_rate_tolerance", 0.0))
        rate_ema: Optional[float] = None
    else:
        lambda_rate = float(lmbda)
        lambda_rate_lr = 0.0
        lambda_rate_min = 0.0
        lambda_rate_max = 0.0
        rate_ema_beta = 0.0
        rate_tolerance = 0.0
        rate_ema = None

    if epochs > 0:
        layer.train()
        log_every = max(1, epochs // 5)
        with torch.enable_grad():
            for epoch in range(epochs):
                optimizer.zero_grad(set_to_none=True)
                recon, _ = layer.reconstruct_weight(training=True, quantized=True)
                mse_loss = torch.mean((recon - target) ** 2)
                rate_loss = layer._last_rate_loss
                if use_adaptive_lambda:
                    total_loss = mse_loss + lambda_rate * (rate_loss - target_rate_value)
                else:
                    total_loss = mse_loss + lmbda * rate_loss
                # Lattice orthogonality regularizer (ec_linear_lattice_eb only)
                lambda_ortho = float(getattr(layer, "lambda_ortho", 0.0) or 0.0)
                if lambda_ortho > 0.0 and hasattr(layer, "orthogonality_loss"):
                    total_loss = total_loss + lambda_ortho * layer.orthogonality_loss()
                total_loss.backward()
                optimizer.step()

                aux_loss_value = torch.tensor(0.0, device=target.device)
                if aux_optimizer is not None:
                    aux_optimizer.zero_grad(set_to_none=True)
                    aux_loss_value = layer.aux_loss()
                    aux_loss_value.backward()
                    aux_optimizer.step()

                if use_adaptive_lambda:
                    rate_now = float(rate_loss.item())
                    if rate_ema is None:
                        rate_ema = rate_now
                    else:
                        rate_ema = rate_ema_beta * rate_ema + (1.0 - rate_ema_beta) * rate_now
                    rate_error = rate_ema - target_rate_value
                    if abs(rate_error) < rate_tolerance:
                        rate_error = 0.0
                    lambda_rate = float(
                        max(
                            lambda_rate_min,
                            min(lambda_rate_max, lambda_rate + lambda_rate_lr * rate_error),
                        )
                    )

                loss_value = float(total_loss.item())
                if loss_value < best_loss:
                    best_loss = loss_value
                    best_state = _clone_best_state(layer)

                row = {
                    "stage": "train",
                    "epoch": float(epoch + 1),
                    "loss": loss_value,
                    "mse_loss": float(mse_loss.item()),
                    "rate_loss": float(rate_loss.item()),
                    "aux_loss": float(aux_loss_value.item()),
                    "lambda_rate": float(lambda_rate),
                }
                history.append(row)
                _ecft_log_append_train(log_path, row)
                if bool(getattr(args, "verbose", False)) and (
                    ((epoch + 1) % log_every == 0) or (epoch == 0) or (epoch + 1 == epochs)
                ):
                    if use_adaptive_lambda:
                        glog.info(
                            f"{args.layer_idx}_{args.layer_name} [ECFT] epoch {epoch + 1}/{epochs} "
                            f"loss={row['loss']:.6f} mse={row['mse_loss']:.6f} "
                            f"rate={row['rate_loss']:.6f} aux={row['aux_loss']:.6f} "
                            f"target={target_rate_value:.6f} lambda={lambda_rate:.6f}"
                        )
                    else:
                        glog.info(
                            f"{args.layer_idx}_{args.layer_name} [ECFT] epoch {epoch + 1}/{epochs} "
                            f"loss={row['loss']:.6f} mse={row['mse_loss']:.6f} "
                            f"rate={row['rate_loss']:.6f} aux={row['aux_loss']:.6f}"
                        )

    layer.load_state_dict(best_state, strict=True)
    layer.eval()

