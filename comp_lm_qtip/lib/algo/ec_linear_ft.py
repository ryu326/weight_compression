def _layer_variant(layer) -> str:
    mod = type(layer).__module__
    if "lattice_eb" in mod:
        return "lattice_eb"
    if "parametric" in mod:
        return "parametric"
    return "compressai"


def load_pretrained_ec_state(layer, pretrained_path: str, strict_variant: bool = True) -> None:
    """Load `qs` / `entropy_bottleneck` / (lattice_eb only) `B` from a
    pretrained-on-Gaussian checkpoint (produced by `pretrain_ec_gaussian.py`)
    into an already-constructed EntropyConstrainedLinear.
    Does NOT touch latent / left_diag / right_diag — those are per-layer."""
    pt = torch.load(pretrained_path, map_location="cpu", weights_only=False)
    expected_variant = str(pt.get("variant", "compressai"))
    actual_variant = _layer_variant(layer)

    if strict_variant and expected_variant != actual_variant:
        raise ValueError(
            f"Pretrained variant '{expected_variant}' does not match layer "
            f"variant '{actual_variant}'. Rebuild the pretraining with the "
            f"matching --variant, or set strict_variant=False."
        )

    dev = layer.latent.device
    dtype = layer.latent.dtype

    # qs
    if "qs" in pt and pt["qs"] is not None:
        layer.qs = pt["qs"].to(device=dev, dtype=torch.float32).reshape(())

    # entropy bottleneck — drop CDF-table buffers (they'll be regenerated by
    # update_entropy_model below once medians/quantiles are loaded).
    eb_state_cpu = pt.get("entropy_bottleneck")
    if eb_state_cpu is not None:
        eb_state = {
            k: v.to(device=dev, dtype=torch.float32)
            for k, v in eb_state_cpu.items()
            if k not in ("_offset", "_quantized_cdf", "_cdf_length")
        }
        missing, unexpected = layer.entropy_bottleneck.load_state_dict(eb_state, strict=False)
        if unexpected:
            glog.warning(f"[ec pretrained] EB load unexpected keys: {list(unexpected)}")
        # Rebuild CDF tables from the loaded medians/quantiles
        layer.update_entropy_model(force=True, update_quantiles=False)

    # B (lattice_eb only)
    if actual_variant == "lattice_eb":
        if "B" not in pt:
            raise ValueError("lattice_eb layer but checkpoint has no B tensor")
        B_pt = pt["B"]
        if tuple(layer.B.shape) != tuple(B_pt.shape):
            raise ValueError(
                f"B shape mismatch: layer={tuple(layer.B.shape)} vs "
                f"pretrained={tuple(B_pt.shape)} — did you use matching --lattice_dim?"
            )
        layer.B.data.copy_(B_pt.to(device=dev, dtype=dtype))


def _build_ec_layer(args, in_features: int, out_features: int, decoder_type: str, rht_seed: Optional[int], device, dtype):
    """Factory: pick compressai, parametric, or lattice_eb EntropyConstrainedLinear based on args."""
    entropy_model = str(getattr(args, "ecft_entropy_model", "compressai")).lower()
    chunk_rows = int(getattr(args, "ec_entropy_chunk_rows", 0) or 0)

    if entropy_model == "parametric":
        num_g = int(getattr(args, "ecft_num_gaussian", 3))
        num_l = int(getattr(args, "ecft_num_laplacian", 3))
        layer = _ECLinearParametric(
            in_features=in_features,
            out_features=out_features,
            bias=False,
            decoder_type=decoder_type,
            entropy_bottleneck_kwargs={"num_gaussian": num_g, "num_laplacian": num_l},
            rht_seed=rht_seed,
            device=device,
            dtype=dtype,
        )
        if chunk_rows > 0:
            layer.entropy_chunk_rows = chunk_rows
        return layer
    if entropy_model == "lattice_eb":
        lattice_dim = int(getattr(args, "ecft_lattice_dim", 4))
        lambda_ortho = float(getattr(args, "ecft_lambda_ortho", 0.0) or 0.0)
        B_init = str(getattr(args, "ecft_B_init", "identity"))
        B_init_scale = getattr(args, "ecft_B_init_scale", None)
        B_init_scale = float(B_init_scale) if B_init_scale is not None else None
        layer = _ECLinearLatticeEB(
            in_features=in_features,
            out_features=out_features,
            bias=False,
            decoder_type=decoder_type,
            lattice_dim=lattice_dim,
            lambda_ortho=lambda_ortho,
            B_init=B_init,
            B_init_scale=B_init_scale,
            rht_seed=rht_seed,
            device=device,
            dtype=dtype,
        )
        if chunk_rows > 0:
            layer.entropy_chunk_rows = chunk_rows
        return layer
    if entropy_model == "compressai":
        return _ECLinearCompressAI(
            in_features=in_features,
            out_features=out_features,
            bias=False,
            decoder_type=decoder_type,
            rht_seed=rht_seed,
            device=device,
            dtype=dtype,
        )
    raise ValueError(
        f"--ecft_entropy_model must be 'compressai', 'parametric', or 'lattice_eb', got {entropy_model!r}"
    )


def _maybe_apply_pretrained(layer, args) -> bool:
    """If args.ec_pretrained_path is set, load the checkpoint into `layer`.
    Returns True if a pretrained state was applied."""
    path = getattr(args, "ec_pretrained_path", None)
    if not path:
        return False
    load_pretrained_ec_state(layer, path)
    glog.info(f"[ec pretrained] loaded: {path}")
    return True


